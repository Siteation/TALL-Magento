namespace App\Http\Controllers;

use Illuminate\Support\Facades\Session;

class Success
{
    public function __invoke()
    {
        if (!Session::has('increment-id')) {
            return redirect('/');
        }

        return response()->view('success', [
            'increment-id' => Session::get('increment-id'),
        ]);
    }
}
