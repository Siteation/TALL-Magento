namespace App\DTO\ProductType;

class ConfigurableProductOptionValue
{
    /**
     * @var int
     */
    private $index;

    /**
     * @var string
     */
    private $label;

    public function __construct(
        int $index,
        string $label
    ) {
        $this->index = $index;
        $this->label = $label;
    }

    /**
     * @return int
     */
    public function getIndex(): int
    {
        return $this->index;
    }

    /**
     * @return string
     */
    public function getLabel(): string
    {
        return $this->label;
    }
}
