namespace App\DTO;

class AggregateOption
{
    /**
     * @var int
     */
    private $count;

    /**
     * @var string
     */
    private $label;

    /**
     * @var string
     */
    private $value;

    public function __construct(
        int $count,
        string $label,
        string $value
    ) {
        $this->count = $count;
        $this->label = $label;
        $this->value = $value;
    }

    /**
     * @return int
     */
    public function getCount(): int
    {
        return $this->count;
    }

    /**
     * @return string
     */
    public function getLabel(): string
    {
        return $this->label;
    }

    /**
     * @return string
     */
    public function getValue(): string
    {
        return $this->value;
    }
}
